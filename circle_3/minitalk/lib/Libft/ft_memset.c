#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	size_t			i;
	unsigned char	*p;

	p = (unsigned char *)s;
	i = 0;
	while (n > i)
	{
		*p = c;
		p++;
		i++;
	}
	return (s);
}
