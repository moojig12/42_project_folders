#include "libft.h"

void	ft_putnbr_fd(int nb, int fd)
{
	char	c;

	if (nb == -2147483648)
	{
		ft_putstr_fd("-2147483648", fd);
	}
	else if (nb < 0)
	{
		ft_putchar_fd('-', fd);
		ft_putnbr_fd(-nb, fd);
	}
	else if (nb > 9)
	{
		ft_putnbr_fd(nb / 10, fd);
		c = nb % 10 + 48;
		ft_putchar_fd(c, fd);
	}
	else
	{
		c = nb + 48;
		ft_putchar_fd(c, fd);
	}
	return ;
}
